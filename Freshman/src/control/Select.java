package control;

public class Select {
	public static void main(String[] args) {
		int a = 2;
		
		switch(a) {
		case 0 :
			System.out.println(0);
			break;
		case 1 :
			System.out.println(1);
			break;
		case 2 :
			System.out.println(2);
			break;
		case 3 :
			System.out.println(3);
			break;
		}
	}
}
